# Question 3: Check existence of a key in a hash map.

my_dict = {"apple": 1, "banana": 2, "cherry": 3}

if "banana" in my_dict:
    print("Key 'banana' exists")
else:
    print("Key 'banana' does not exist")

if "grape" in my_dict:
    print("Key 'grape' exists")
else:
    print("Key 'grape' does not exist")